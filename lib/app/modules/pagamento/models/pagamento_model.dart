class PagamentoModel {
  String _rua = "";
  String _numero = "";
  String _cartao = "";
  String _cvv = "";
  
  String _nome = "";

  String get rua {
    return _rua;
  }

  set rua(String value) {
    _rua = rua;
  }

  String get numero {
    return _numero;
  }

  set numero(String value) {
    _numero = numero;
  }

  String get cartao {
    return _cartao;
  }

  set cartao(String value) {
    _cartao = cartao;
  }

  String get cvv {
    return _cvv;
  }

  set cvv(String value) {
    _cvv = cvv;
  }

  String get nome {
    return _nome;
  }

  set nome(String value) {
    _nome = nome;
  }
}
